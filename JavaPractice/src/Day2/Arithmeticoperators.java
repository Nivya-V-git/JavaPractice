package Day2;
public class Arithmeticoperators {
    public static void main(String args[]){
        int a = 17;
        int b = 5;
        System.out.println (a + b); 
        System.out.println (a - b); 
        System.out.println (a * b); 
        System.out.println (a / b); 
        System.out.println (a % b);
    }
}